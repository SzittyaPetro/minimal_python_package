def add(a: int, b: int) -> int:
    """
    Add two integers.

    Parameters
    ----------
    a : int
        The first number.
    b : int
        The second number.

    Returns
    -------
    int
        The sum of a and b.
    """
    return a + b


def multiply(a: int, b: int) -> int:
    """
    Multiply two numbers together.

    Parameters
    ----------
    a : int
        The first factor.
    b : int
        The second factor.

    Returns
    -------
    int
        The product of a and b.
    """
    return a * b